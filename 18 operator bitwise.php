$c = $a ^ $b;
echo "$a ^ $b = $c";
echo "<br>";
echo "<hr>";

// Shift Left
$c = $a << $b;
echo "$a << $b = $c";
echo "<br>";
echo "<hr>";

// Shift Right
$c = $a >> $b;
echo "$a >> $b = $c";
